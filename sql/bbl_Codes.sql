SELECT p.Player_Name,
       SUM(b.Runs) AS Runs,
       SUM(bw.Wickets) AS Wickets
FROM PLAYER p
JOIN BATTING b ON p.Player_ID = b.Player_ID
JOIN BOWLING bw ON p.Player_ID = bw.Player_ID
GROUP BY p.Player_Name
HAVING SUM(b.Runs) > 200 AND SUM(bw.Wickets) > 10;

