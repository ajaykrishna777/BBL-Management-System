DROP TABLE FIELDING CASCADE CONSTRAINTS;

DROP TABLE BOWLING CASCADE CONSTRAINTS;

DROP TABLE BATTING CASCADE CONSTRAINTS;

-- THEN PARENT TABLES
DROP TABLE MATCH CASCADE CONSTRAINTS;

DROP TABLE PLAYER CASCADE CONSTRAINTS;

DROP TABLE TEAM CASCADE CONSTRAINTS;

-- LAST (INDEPENDENT)
DROP TABLE USER_LOGIN CASCADE CONSTRAINTS;

CREATE TABLE PLAYER (
    Player_ID    VARCHAR2(10)   PRIMARY KEY,
    Player_Name  VARCHAR2(100)  NOT NULL
);
CREATE TABLE TEAM (
    Team_ID      VARCHAR2(10)   PRIMARY KEY,
    Team_Name    VARCHAR2(100)  NOT NULL UNIQUE,
    City         VARCHAR2(50),
    Home_Ground  VARCHAR2(100),
    Coach        VARCHAR2(100)
);
CREATE TABLE MATCH (
    Match_ID        VARCHAR2(10)  PRIMARY KEY,
    Match_Date      DATE          NOT NULL,
    Venue           VARCHAR2(100),
    Team1_ID        VARCHAR2(10)  NOT NULL,
    Team2_ID        VARCHAR2(10)  NOT NULL,
    Winner_Team_ID  VARCHAR2(10),
    CONSTRAINT fk_m_t1  FOREIGN KEY (Team1_ID)
        REFERENCES TEAM(Team_ID),
    CONSTRAINT fk_m_t2  FOREIGN KEY (Team2_ID)
        REFERENCES TEAM(Team_ID),
    CONSTRAINT fk_m_win FOREIGN KEY (Winner_Team_ID)
        REFERENCES TEAM(Team_ID),
    CONSTRAINT chk_teams CHECK (Team1_ID <> Team2_ID)
);
CREATE TABLE BATTING (
    Bat_ID      VARCHAR2(10)  PRIMARY KEY,
    Player_ID   VARCHAR2(10)  NOT NULL,
    Match_ID    VARCHAR2(10)  NOT NULL,
    Runs        NUMBER(5)     DEFAULT 0 CHECK (Runs >= 0),
    Balls       NUMBER(5)     CHECK (Balls >= 0),
    Strike_Rate NUMBER(6,2)   CHECK (Strike_Rate >= 0),
    CONSTRAINT fk_bat_pl FOREIGN KEY (Player_ID)
        REFERENCES PLAYER(Player_ID) ON DELETE CASCADE,
    CONSTRAINT fk_bat_mt FOREIGN KEY (Match_ID)
        REFERENCES MATCH(Match_ID)
);
CREATE TABLE BOWLING (
    Bowl_ID   VARCHAR2(10)  PRIMARY KEY,
    Player_ID VARCHAR2(10)  NOT NULL,
    Match_ID  VARCHAR2(10)  NOT NULL,
    Overs     NUMBER(5,1)   CHECK (Overs >= 0),
    Wickets   NUMBER(3)     DEFAULT 0 CHECK (Wickets >= 0),
    Economy   NUMBER(5,2)   CHECK (Economy >= 0),
    CONSTRAINT fk_bwl_pl FOREIGN KEY (Player_ID)
        REFERENCES PLAYER(Player_ID) ON DELETE CASCADE,
    CONSTRAINT fk_bwl_mt FOREIGN KEY (Match_ID)
        REFERENCES MATCH(Match_ID)
);
CREATE TABLE FIELDING (
    Field_ID  VARCHAR2(10)  PRIMARY KEY,
    Player_ID VARCHAR2(10)  NOT NULL,
    Match_ID  VARCHAR2(10)  NOT NULL,
    Catches   NUMBER(3)     DEFAULT 0 CHECK (Catches >= 0),
    Runouts   NUMBER(3)     DEFAULT 0 CHECK (Runouts >= 0),
    CONSTRAINT fk_fld_pl FOREIGN KEY (Player_ID)
        REFERENCES PLAYER(Player_ID) ON DELETE CASCADE,
    CONSTRAINT fk_fld_mt FOREIGN KEY (Match_ID)
        REFERENCES MATCH(Match_ID)
);
CREATE TABLE WICKETKEEPING (
    WK_ID     VARCHAR2(10)  PRIMARY KEY,
    Player_ID VARCHAR2(10)  NOT NULL,
    Match_ID  VARCHAR2(10)  NOT NULL,
    Stumpings NUMBER(3)     DEFAULT 0 CHECK (Stumpings >= 0),
    Catches   NUMBER(3)     DEFAULT 0 CHECK (Catches >= 0),
    CONSTRAINT fk_wk_pl FOREIGN KEY (Player_ID)
        REFERENCES PLAYER(Player_ID) ON DELETE CASCADE,
    CONSTRAINT fk_wk_mt FOREIGN KEY (Match_ID)
        REFERENCES MATCH(Match_ID)
);
CREATE TABLE PARTNERSHIP (
    Partnership_ID VARCHAR2(10)  PRIMARY KEY,
    Match_ID       VARCHAR2(10)  NOT NULL,
    Player1_ID     VARCHAR2(10)  NOT NULL,
    Player2_ID     VARCHAR2(10)  NOT NULL,
    Runs           NUMBER(4)     CHECK (Runs >= 0),
    CONSTRAINT fk_pt_mt FOREIGN KEY (Match_ID)
        REFERENCES MATCH(Match_ID),
    CONSTRAINT fk_pt_p1 FOREIGN KEY (Player1_ID)
        REFERENCES PLAYER(Player_ID),
    CONSTRAINT fk_pt_p2 FOREIGN KEY (Player2_ID)
        REFERENCES PLAYER(Player_ID),
    CONSTRAINT chk_pt_players
        CHECK (Player1_ID <> Player2_ID)
);
CREATE TABLE USER_LOGIN (
    User_ID    NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    Username   VARCHAR2(50)   UNIQUE NOT NULL,
    Password   VARCHAR2(100)  NOT NULL,
    Role       VARCHAR2(10)   DEFAULT 'VIEWER'
               CHECK (Role IN ('ADMIN','VIEWER')),
    Created_At TIMESTAMP      DEFAULT CURRENT_TIMESTAMP
);
