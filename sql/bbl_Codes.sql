BEFORE INSERT OR UPDATE ON BATTING
FOR EACH ROW
BEGIN
    IF :NEW.Runs < 0 OR :NEW.Balls < 0 OR :NEW.Strike_Rate < 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Invalid batting values');
    END IF;
END;
