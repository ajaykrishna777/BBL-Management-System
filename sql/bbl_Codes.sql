    Audit_ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    Player_ID VARCHAR2(10),
    Player_Name VARCHAR2(100),
    Deleted_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE OR REPLACE TRIGGER trg_player_delete
BEFORE DELETE ON PLAYER
FOR EACH ROW
BEGIN
    INSERT INTO PLAYER_AUDIT(Player_ID, Player_Name)
    VALUES (:OLD.Player_ID, :OLD.Player_Name);
END;
