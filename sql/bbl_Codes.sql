-- PROCEDURES
-- Get Player Full Stats
CREATE OR REPLACE PROCEDURE get_player_stats(
    p_id IN VARCHAR2,
    cur OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN cur FOR
    SELECT p.Player_Name,
           NVL(SUM(b.Runs),0) AS Runs,
           NVL(SUM(bw.Wickets),0) AS Wickets,
           NVL(SUM(f.Catches),0) AS Catches,
           NVL(SUM(wk.Stumpings),0) AS Stumpings
    FROM PLAYER p
    LEFT JOIN BATTING b ON p.Player_ID = b.Player_ID
    LEFT JOIN BOWLING bw ON p.Player_ID = bw.Player_ID
    LEFT JOIN FIELDING f ON p.Player_ID = f.Player_ID
    LEFT JOIN WICKETKEEPING wk ON p.Player_ID = wk.Player_ID
    WHERE p.Player_ID = p_id
    GROUP BY p.Player_Name;
END;
/

-- Get Team Wins
CREATE OR REPLACE PROCEDURE get_team_wins(
    t_id IN VARCHAR2,
    wins OUT NUMBER
)
AS
BEGIN
    SELECT COUNT(*) INTO wins
    FROM MATCH
    WHERE Winner_Team_ID = t_id;
END;
/

-- Get Player Partnerships
CREATE OR REPLACE PROCEDURE get_player_partnerships(
    p_id IN VARCHAR2,
    cur OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN cur FOR
    SELECT pt.Partnership_ID, pt.Runs
    FROM PARTNERSHIP pt
    WHERE pt.Player1_ID = p_id OR pt.Player2_ID = p_id;
END;
/

-- FUNCTIONS
-- Total Runs
CREATE OR REPLACE FUNCTION total_runs(p_id VARCHAR2)
RETURN NUMBER
AS
    r NUMBER;
BEGIN
    SELECT NVL(SUM(Runs),0) INTO r
    FROM BATTING
    WHERE Player_ID = p_id;
    RETURN r;
END;
/

-- Total Wickets
CREATE OR REPLACE FUNCTION total_wickets(p_id VARCHAR2)
RETURN NUMBER
AS
    w NUMBER;
BEGIN
    SELECT NVL(SUM(Wickets),0) INTO w
    FROM BOWLING
    WHERE Player_ID = p_id;
    RETURN w;
END;
/

-- Highest Partnership
CREATE OR REPLACE FUNCTION max_partnership(p_id VARCHAR2)
RETURN NUMBER
AS
    m NUMBER;
BEGIN
    SELECT NVL(MAX(Runs),0) INTO m
    FROM PARTNERSHIP
    WHERE Player1_ID = p_id OR Player2_ID = p_id;
    RETURN m;
END;
/

-- TRIGGERS
-- Validate Batting
